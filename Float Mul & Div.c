#include <stdio.h>
#include <stdlib.h>

int main()
{
    float num1 = 2.5;
    float num2 = 3.2;
    float product;
    product = num1 * num2;
    printf("The product of %f and %f is %f\n", num1, num2, product);
    float dividend = 10.0;
    float divisor = 3.0;
    float quotient;
    quotient = dividend / divisor;
    printf("The quotient of %f divided by %f is %f\n", dividend, divisor, quotient);


    return 0;
}

